#include "../includes/cub3d.h"

void	print_map(t_map *map)
{
	int	i;

	i = 0;
	while (map->tab[i])
	{
		ft_putstr_fd(map->tab[i], 1);
		i++;
	}
	ft_putchar_fd('\n', 1);
}
